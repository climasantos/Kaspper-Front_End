// Algoritmo de repetição em ordem decrescente.
for (i = 10; i >= 1; i--) {
    console.log(i);
}



// Algoritmo que calcule e imprima a tabuada do 7 (1 a 10)
for(i = 1; i <= 10; i++){
    let sete = i * 7;
    console.log(`7 x ${i} = ${sete} `);
}


