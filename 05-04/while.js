// Exemplos utilizando While e Do While
let salario = 5001;

while(salario < 5000){
    salario += 200;
    console.log(salario);
}

// exemplo com o do while(faz um teste, analisa ao menos uma vez se o valor do input bate)
let aumento = 301;

do{
    aumento += 50;
    console.log(aumento);
} while (aumento < 300);
