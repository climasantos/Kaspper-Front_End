let nome = ("carlos lima")
let sobrenome = ("andré")

console.log('nome');
console.log(typeof'nome');

//o método charAt retorna o caractere especificado a partir de uma string. Sendo o caractere especifícado o "c".
console.log(nome.charAt(0));

//o método charCodeAt indíca a posição do caractere.
console.log(nome.charCodeAt(1));

//o método concat retorna um novo array contendo todos os arrays ou valores passados como parâmetro.

console.log(nome.concat(sobrenome));

//o método indexOf retorna o primeiro índice em que o elemento pode ser encontrado no array, retorna -1 caso o mesmo não esteja presente.

console.log(nome.indexOf("a"));

//o método lastIndexOf retorna o ultimo índice que um certo elemento pode ser encontrado no array, ou -1 se o elemento não estiver presente. O array é pesquisado de trás para frente, começando pelo fromIndex.

console.log(nome.lastIndexOf("a"));





